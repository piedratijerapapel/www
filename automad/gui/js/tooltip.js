/*
 * 	Remove tooltip component on touch screens.
 */

+function(Automad, $) {
		
	$(document).on('ready ajaxComplete', function() {
		setTimeout(function() {
			$('html.uk-touch [data-uk-tooltip]').removeAttr('data-uk-tooltip');
		}, 200);
	});
	
}(window.Automad = window.Automad || {}, jQuery);